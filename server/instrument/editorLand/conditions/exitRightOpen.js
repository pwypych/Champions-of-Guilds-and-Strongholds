// @format

'use strict';

const debug = require('debug')('cogs:exitRightOpenClosed');
const PF = require('pathfinding');
const _ = require('lodash');

module.exports = () => {
  return (req, res, next) => {
    (function init() {
      debug('// Checks if parcel generated by landGenerateMapPost fullfills condition if exitRight needs to be opened');
      debug('// If it does not need to be opened, we do not need to check for closed, next parcel will ensure that');

      if (!res.locals.conditions) {
        res.locals.conditions = [];
      }

      res.locals.conditions.push((parcel, abstractParcel) => {
        // return true if condition is met
        return validateCondition(parcel, abstractParcel);
      });

      next();
    })();

    function validateCondition(parcel, abstractParcel) {
      if (!checkShouldBeOpen(abstractParcel)) {
        debug('validateCondition: Skip! Does not need to be opened');
        return true;
      }

      const figureCoords = calculateFigureCoords(parcel);

      const parcelCopy = JSON.parse(JSON.stringify(parcel));
      const parcelExtended = addColumnToParcel(parcelCopy);

      const isValid = validatePath(parcelExtended, figureCoords);

      if (isValid) {
        debug('validateCondition: Correct! exitRight should be opened and is open');
      } else {
        debug('validateCondition: Wrong! exitRight should be opened but is closed');
      }

      return isValid;
    }

    function checkShouldBeOpen(abstractParcel) {
      let isRequired = false;
      abstractParcel.conditions.forEach((condition) => {
        if (condition.name === 'exitRight') {
          isRequired = true;
        }
      });

      return isRequired;
    }

    function calculateFigureCoords(parcel) {
      let figureCoords = {x: 3, y: 3}; // default middle
      parcel.forEach((row, y) => {
        row.forEach((figure, x) => {
          if (figure !== 'empty' && figure !== 'tree') {
            figureCoords = {x: x, y: y};
          }
        });
      });
      return figureCoords;
    }

    function addColumnToParcel(parcel) {
      parcel.forEach((row, y) => {
        parcel[y].push('empty');
      });
      return parcel;
    }

    // validate between figure and top right corner on extended column
    function validatePath(parcelExtended, figureCoords) {
      const height = parcelExtended.length;
      const width = parcelExtended[0].length;
      const topRightCorner = {x: 7, y: 0};

      const grid = new PF.Grid(width, height);

      // define collidables
      parcelExtended.forEach((row, y) => {
        row.forEach((figure, x) => {
          if (figure === 'tree') {
            grid.setWalkableAt(x, y, false);
          }
        });
      });

      const finder = new PF.AStarFinder({ allowDiagonal: false });
      const path = finder.findPath(
        figureCoords.x,
        figureCoords.y,
        topRightCorner.x,
        topRightCorner.y,
        grid
      );

      if (_.isEmpty(path)) {
        return false;
      }

      return true;
    }
  };
};
